// ADC driver functions
void ADC1SS3_Init(void);
uint16_t ADC1SS3_In(void);
uint8_t ADCValue_To_X_AXIS(uint16_t ADCValue, uint8_t max_x_axis);

// Bitmap Data
extern const unsigned char SmallEnemyPointA[][200];
extern const unsigned char SmallEnemyPointB[][200];
extern const unsigned char PlayerShip0[];
extern const unsigned char Missile0[];
extern const unsigned char Missile2[];
extern const unsigned char SmallExplosion0[];

// Game Structures 
typedef struct {
  uint8_t x;
  uint8_t y;
  const uint8_t *imageA;
  const uint8_t *imageB;
  bool alive;
  bool open;
} Enemy_t;

//Player structure 
typedef struct {
  uint8_t x;
  uint8_t y;
  const uint8_t *image;
} Player_t;

// Bullet structure 
typedef struct {
  uint8_t x;
  uint8_t y;
  bool alive;
} Bullet_t;

#define ENEMY_COUNT 8
#define ENEMY_W 16
#define ENEMY_H 10

Enemy_t Enemies[ENEMY_COUNT];
Player_t Player;  // player spaceship
Bullet_t Bullet;  // single bullet instance

bool game_on = false;
bool refresh = false;

//Score variable 
uint8_t score = 0;

//Function Prototypes
void System_Init(void);
void Game_Init(void);
void Start_Prompt(void);
void End_Prompt(void);
void SysTick_Handler(void);
void GPIOPortF_Handler(void);
extern void EnableInterrupts(void);
extern void DisableInterrupts(void);
void move(void);
void draw(void);

// MAIN 
int main(void) {
  System_Init();

  while (1) {
    Start_Prompt();

    while (!game_on) {}  // wait for SW2 to start

    Game_Init();
    while (game_on) {
      if (refresh) {
        refresh = false;
        move();
				draw();
      }
    }

    End_Prompt();
  }
}

void System_Init(void) {
  PLL_Init();          // 80 MHz
  Nokia5110_Init();
  Switch_Init();
  SysTick_Init();      // 80 MHz SysTick base
  ADC1SS3_Init();      // initialize ADC1 (PE5/AIN8)
  NVIC_ST_CTRL_R = 0;                  // disable SysTick
  NVIC_ST_RELOAD_R = 8000000 - 1;      // 0.1 s interval (10 Hz)
  NVIC_ST_CURRENT_R = 0;
  NVIC_ST_CTRL_R = 0x07;               // enable SysTick INT + core clock

  Nokia5110_Clear();
  Sound_Init();      
  EnableInterrupts();
}


// initialize 4 enemies on the top row and row below with different images
void Game_Init(void) {
  for (int i = 0; i < ENEMY_COUNT; i++) {
    Enemies[i].x = 20 * (i%4);       // enemy 1 at x=1, others 20px apart
    Enemies[i].y = 10 + 15*(i/4);               // all enemies on top row
    Enemies[i].imageA = SmallEnemyPointA[i%3]; // different shapes
    Enemies[i].imageB = SmallEnemyPointB[i%3];
    Enemies[i].alive = true;
    Enemies[i].open = false;
  }

  //player position
  uint16_t adcValue = ADC1SS3_In();
  Player.x = ADCValue_To_X_AXIS(adcValue, 84 - 18); 
  Player.y = 47;             // bottom of screen
  Player.image = PlayerShip0;

  Bullet.alive = false;
  Bullet.x = 0;
  Bullet.y = 0;

  score = 0;                 // reset score at start
}

void move(void) {
  uint8_t num_life = 0;

  // Move Enemies 
  for (int i = 0; i < ENEMY_COUNT; i++) {
    if (!Enemies[i].alive) continue;

    Enemies[i].x += 1;
    Enemies[i].open = !Enemies[i].open;

    if (Enemies[i].x + ENEMY_W >= 84) {
      Enemies[i].alive = false;
    } else {
      num_life++;
    }
  }

  if (num_life == 0) {
    game_on = false;
  }

  // Move Player (ADC) 
  uint16_t adcValue = ADC1SS3_In();
  Player.x = ADCValue_To_X_AXIS(adcValue, 84 - 18);

  // Move Bullet 
  if (Bullet.alive) {

    Nokia5110_PrintBMP(Bullet.x, Bullet.y, Missile2, 0);

    if (Bullet.y <= 10) {
      Bullet.alive = false;
    } else {
      Bullet.y -= 2;

      // collision check 
      for (int i = 0; i < ENEMY_COUNT; i++) {
        if (!Enemies[i].alive) continue;

        if (Bullet.x + 2 >= Enemies[i].x &&
            Bullet.x <= Enemies[i].x + ENEMY_W &&
            Bullet.y <= Enemies[i].y &&
            Bullet.y + 4 >= Enemies[i].y - ENEMY_H) {

          Enemies[i].alive = false;
          Bullet.alive = false;

          score++;

          Sound_Killed();   //enemy explosion sound

          Nokia5110_PrintBMP(Enemies[i].x, Enemies[i].y, SmallExplosion0, 0);
          Nokia5110_DisplayBuffer();
          SysTick_Wait100ms(5);// 0.5s
							
          return;
        }
      }
    }
  }
}

void draw(void) {
  static bool bullet_was_alive = false;   // detect first frame of bullet

  Nokia5110_ClearBuffer();

  // Draw Enemies 
  for (int i = 0; i < ENEMY_COUNT; i++) {
    if (Enemies[i].alive) {
      const uint8_t *img = Enemies[i].open ? Enemies[i].imageB : Enemies[i].imageA;
      Nokia5110_PrintBMP(Enemies[i].x, Enemies[i].y, img, 0);
    }
  }

  // Draw Player
  Nokia5110_PrintBMP(Player.x, Player.y, Player.image, 0);

  // Draw Bullet 
  if (Bullet.alive) {

    if (!bullet_was_alive) {
      Sound_Shoot();        // shoot sound 
    }

    Nokia5110_PrintBMP(Bullet.x, Bullet.y, Missile0, 0);
    bullet_was_alive = true;

  } else {
    bullet_was_alive = false;
  }

  Nokia5110_DisplayBuffer();
}


//Starting text
void Start_Prompt(void) {
  Nokia5110_Clear();
  Nokia5110_SetCursor(0, 1);
  Nokia5110_OutString("Space");
  Nokia5110_SetCursor(0, 2);
  Nokia5110_OutString("Invader");
  Nokia5110_SetCursor(0, 3);
  Nokia5110_OutString("Press SW2");
  Nokia5110_SetCursor(0, 4);
  Nokia5110_OutString("To Start");
}
//End of game score 
void End_Prompt(void) {
  char scoreStr[5];
  sprintf(scoreStr, "%d", score);  // format real score

  Nokia5110_Clear();
  Nokia5110_SetCursor(0, 1);
  Nokia5110_OutString("Game Over");
  Nokia5110_SetCursor(0, 2);
  Nokia5110_OutString("Nice Try!");
  Nokia5110_SetCursor(0, 3);
  Nokia5110_OutString("Your Score:");
  Nokia5110_SetCursor(5, 4);
  Nokia5110_OutString(scoreStr);

  SysTick_Wait100ms(30);  // ~3s delay
}

// Called every 0.1 s (10 Hz)
void SysTick_Handler(void) {
  refresh = true;
}

// SW1 = PF4 (rising edge release to shoot)
// SW2 = PF0 (falling edge press to start)
void GPIOPortF_Handler(void) {
  // SW2 pressed (falling edge)
  if (GPIO_PORTF_RIS_R & 0x01) {
    GPIO_PORTF_ICR_R = 0x01; // clear flag
    if (!game_on) {
      game_on = true;        // start the game
    }
  }

  // SW1 released (rising edge)
  if (GPIO_PORTF_RIS_R & 0x10) {
    GPIO_PORTF_ICR_R = 0x10; // clear flag
    if (game_on && !Bullet.alive) {
      Bullet.alive = true;           // activate bullet
      Bullet.x = Player.x + 8;       // shoot from center of ship
      Bullet.y = Player.y - 4;       // just above ship
      Sound_Shoot();               // Bullet sound
    }
  }
}

